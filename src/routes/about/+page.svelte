<h1>Ninhursag</h1>

<h2>Introduction</h2>

<p>Ninhursag is a visual representation of US Geologic Survey&rsquo;s
statistics of mineral resources and other commodities usage from the
start of the 20th century (year 1900 onwards) using interactive
diagrams.</p>

<p>The inspiration comes from the book
<a href="http://en.wikipedia.org/wiki/The_Limits_to_Growth">&ldquo;The
Limits to Growth&rdquo;</a> commissioned by the club of Rome in 1972,
and the accompanying computer programmes. Although the book have had
much critique about it&rsquo;s methodology, I think it&rsquo;s
important that people understand where we stand on the sustainability
of natural resources we each use every day.</p>

<p>Other source of inspiration for making this project is that I used
to work in an advertising agency, and back then did some graphic
design for some well known companies, and also designed brochures for
one mining equipment manufacturing company.</p>

<p>That got me thinking, almost nothing comes just from a factory but
there is a lot of machinery, manpower, dynamite, drilling, usually
much more than 95 percent of wasted rock from the raw ore, mixed with
various chemicals dumped into the environment before the factory can
even start the assembly of a product. Not to mention transporting the
raw materials back and forth the globe using fossil fuels.</p>

<p>Also of interest is
	<a href="http://en.wikipedia.org/wiki/Hubbert_peak_theory">Hubbert
		peak theory</a>
	regarding use of oil in particular.
</p>

<h2>Frequently asked questions</h2>

<h3>1. Q: What does the name mean?</h3>

<ul>
	<li>A: In Sumerian mythology,
		<a href="http://en.wikipedia.org/wiki/Ninhursag">Ninhursag</a>
		was a mother goddess of the mountains, and one of the seven
		great deities of Sumer. She is principally a fertility
		goddess. Temple hymn sources identify her as the &lsquo;true
		and great lady of heaven&rsquo; (possibly in relation to her
		standing on the mountain) and kings of Sumer were
		&lsquo;nourished by Ninhursag&rsquo;s milk&rsquo;. Her hair is
		sometimes depicted in an omega shape, and she at times wears a
		horned head-dress and tiered skirt, often with bow cases at
		her shoulders, and not infrequently carries a mace or baton
		surmounted by an omega motif or a derivation, sometimes
		accompanied by a lion cub on a leash. She is the tutelary
		deity to several Sumerian leaders.  According to legend her
		name was changed from Ninmah to Ninhursag by her son Ninurta
		in order to commemorate his creation of the mountains.
	</li>
</ul>
